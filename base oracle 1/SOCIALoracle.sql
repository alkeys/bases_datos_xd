DROP TABLE Likes;
DROP TABLE Friend;
DROP TABLE Highschooler;

-- 2. CREACIÓN DE LA ESTRUCTURA
CREATE TABLE Highschooler (
    ID NUMBER(5) PRIMARY KEY,
    name VARCHAR2(50) NOT NULL,
    grade NUMBER(2) NOT NULL
);

CREATE TABLE Friend (
    ID1 NUMBER(5),
    ID2 NUMBER(5),
    PRIMARY KEY (ID1, ID2),
    CONSTRAINT fk_friend_id1 FOREIGN KEY (ID1) REFERENCES Highschooler(ID),
    CONSTRAINT fk_friend_id2 FOREIGN KEY (ID2) REFERENCES Highschooler(ID)
);

CREATE TABLE Likes (
    ID1 NUMBER(5),
    ID2 NUMBER(5),
    PRIMARY KEY (ID1, ID2),
    CONSTRAINT fk_likes_id1 FOREIGN KEY (ID1) REFERENCES Highschooler(ID),
    CONSTRAINT fk_likes_id2 FOREIGN KEY (ID2) REFERENCES Highschooler(ID)
);

