-- 3. CREACIÓN DE ESTRUCTURA
CREATE TABLE MOVIE (
    mID NUMBER(5) PRIMARY KEY,
    title VARCHAR2(200) NOT NULL,
    year NUMBER(5) NOT NULL,
    director VARCHAR2(200)
);

CREATE TABLE REVIEWER (
    rID NUMBER(5) PRIMARY KEY,
    name VARCHAR2(200) NOT NULL
);

CREATE TABLE RATING (
    rID NUMBER(5),
    mID NUMBER(5),
    stars NUMBER(5) NOT NULL,
    ratingDate DATE,
    CONSTRAINT fk_rating_reviewer FOREIGN KEY (rID) REFERENCES REVIEWER(rID),
    CONSTRAINT fk_rating_movie FOREIGN KEY (mID) REFERENCES MOVIE(mID)
);

